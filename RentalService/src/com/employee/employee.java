package com.employee;

public class employee {
	
	
			private int id;
			private String name;
			private String phone;
			private String email;
			private String position;
			private String salary;
			private String gender;
			
			
			
			public employee(int id, String name, String phone, String email, String position, String salary,
					String gender) {
				
				this.id = id;
				this.name = name;
				this.phone = phone;
				this.email = email;
				this.position = position;
				this.salary = salary;
				this.gender = gender;
			}



			public int getId() {
				return id;
			}



			public String getName() {
				return name;
			}



			public String getPhone() {
				return phone;
			}



			public String getEmail() {
				return email;
			}



			public String getPosition() {
				return position;
			}



			public String getSalary() {
				return salary;
			}



			public String getGender() {
				return gender;
			}



		
			
			
			
			
		
	}


